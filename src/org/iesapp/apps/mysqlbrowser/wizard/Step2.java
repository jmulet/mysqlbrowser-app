    private void loadMeta()
    {
        if(jComboBox1.getSelectedIndex()<0) {
            return;
        }
        
         //Set the sheet and provides metaInformation
        selectedSheet = workbook.getSheet((String) jComboBox1.getSelectedItem());
        int ncols = selectedSheet.getColumns();
        jFiles.setText(""+selectedSheet.getRows());
        jColumnes.setText(""+ncols);
        
        //Carrega els noms de les columnes dins dels comboBoxs
        excelCols = new String[ncols+1];
        excelCols[0] = "";
        String[] columnNames = new String[ncols];
        for(int i=0; i<ncols; i++)
        {
            Cell cell = selectedSheet.getCell(i, 0);
            columnNames[i] = columnLetter(i);
            excelCols[i+1]= "["+columnLetter(i)+"]: "+ cell.getContents()+", ...";
            if( !((String) jTable1.getValueAt(i,0)).contains("AUTO") ) {
                jTable1.setValueAt(excelCols[i+1], i, 1);
            }
        }
        jTable1.getColumnModel().getColumn(1).setCellEditor(new MyComboBoxEditor(excelCols));
        
        //Carrega el full excel dins la taula2
        //Crea primer un model
        modelTable2 = new javax.swing.table.DefaultTableModel(new Object [][]{ },  columnNames);
        jTable2.setModel(modelTable2);
                
        
        for(int i=0; i<selectedSheet.getRows(); i++)
        {
            Object[] obj = new Object[ncols];
            for(int j=0; j< ncols; j++)            
            {
                Cell cell = selectedSheet.getCell(j, i);
                obj[j] = cell.getContents();
            }
            modelTable2.addRow(obj);            
        }
    }
    
    
