    public void setExcelFileName(java.io.File file) {
        
        if(alreadySet) {
            return;
        }
        
        comboModel1 = new DefaultComboBoxModel();
        
        //Gets metainformation of the excel file
          WorkbookSettings ws = new WorkbookSettings();
          ws.setLocale(new Locale("ca", "ES"));
          ws.setEncoding("ISO-8859-1");
          
        try {
            workbook = Workbook.getWorkbook(file, ws);
            String[] sheetNames = workbook.getSheetNames();
            for(String s: sheetNames)
            {
                comboModel1.addElement(s);
            }
            jComboBox1.setModel(comboModel1);        
            
        } catch (IOException ex) {
            Logger.getLogger(Step2.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BiffException ex) {
            Logger.getLogger(Step2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        loadMeta();
        //
        alreadySet =true;
    }
    
    public HashMap<String, Integer> getAssociations()
    {
        HashMap<String, Integer> asoc = new HashMap<String, Integer> ();
        
        for(int i=0; i<jTable1.getRowCount(); i++)
        {
            String taula = (String) jTable1.getValueAt(i, 0);
            boolean isauto = taula.contains("AUTO");
            taula = taula.replace("[PK]", "").replace("[AUTO]", "").trim();
      
            String valor = (String) jTable1.getValueAt(i, 1);
            if(valor.isEmpty() && isauto)
            {
                asoc.put(taula,-1);
            }
            else if(valor.isEmpty() && !isauto)
            {
                asoc.put(taula,-2);
            }
            else
            {
                int pos = -1;
                for(int k=0; k<excelCols.length; k++)
                {
                    if(excelCols[k].equals(valor))
                    {
                        pos = k;
                        break;
                    }
                }
                if(pos>0) {
                    asoc.put(taula, pos-1);
                }
            }
        }
        
        return asoc;
    }
    
