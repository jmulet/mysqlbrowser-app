    private String columnLetter(int i)
    {
        int n = LETTERS.length;
        int res = i % n;
        int quo = (int) (i / (1.*n));
        if(quo==0) {
            return LETTERS[i];
        }
        if(quo==1) {
            return "A"+LETTERS[i-n];
        }
        return "";
    }
    
   

   
